// Command gen はComponentsの型付きアクセサ（GetX/TryX）を生成する。
// componentsパッケージをAST解析し、*ecs.SliceComponent フィールドごとに
// 型付きの取得メソッドを出力することで、呼び出し側の手書き型アサーションを排除する。
//
// 使い方: components ディレクトリで `go generate` するか `go run ./gen` を実行する。
package main

import (
	"bytes"
	"fmt"
	"go/ast"
	"go/format"
	"go/parser"
	"go/token"
	"os"
	"path/filepath"
	"sort"
	"strings"
)

// typeNameOverrides はフィールド名とデータ型名が一致しない例外を補正する。
// 大半のフィールドは「フィールド名 == 型名」だが、以下は異なる。
var typeNameOverrides = map[string]string{
	"DungeonState": "Dungeon",
	"VisualEffect": "VisualEffects",
}

func main() {
	if err := run(); err != nil {
		fmt.Fprintln(os.Stderr, "gen:", err)
		os.Exit(1)
	}
}

func run() error {
	// go generate はcomponentsディレクトリで実行されるため、カレントを解析対象にする
	pkgDir := "."
	files, err := parseComponentFiles(pkgDir)
	if err != nil {
		return err
	}

	sliceFields := collectSliceComponentFields(files)
	declaredTypes := collectDeclaredTypes(files)

	type accessor struct {
		Field string
		Type  string
	}
	var accessors []accessor
	var skipped []string
	for _, field := range sliceFields {
		typeName := field
		if override, ok := typeNameOverrides[field]; ok {
			typeName = override
		}
		// 型が実在するフィールドのみ生成する（取り違えによる誤生成を避ける）
		if !declaredTypes[typeName] {
			skipped = append(skipped, field)
			continue
		}
		accessors = append(accessors, accessor{Field: field, Type: typeName})
	}
	sort.Slice(accessors, func(i, j int) bool { return accessors[i].Field < accessors[j].Field })

	var buf bytes.Buffer
	buf.WriteString("// Code generated by gen; DO NOT EDIT.\n\n")
	buf.WriteString("package components\n\n")
	buf.WriteString("import ecs \"github.com/x-hgg-x/goecs/v2\"\n\n")
	for _, a := range accessors {
		fmt.Fprintf(&buf, "// Get%s はエンティティの%sコンポーネントを取得する。\n", a.Field, a.Field)
		fmt.Fprintf(&buf, "// 保持していない場合はpanicする。呼び出し側が保持を保証している場合に使う。\n")
		fmt.Fprintf(&buf, "func (c *Components) Get%s(entity ecs.Entity) *%s {\n", a.Field, a.Type)
		fmt.Fprintf(&buf, "\treturn c.%s.Get(entity).(*%s)\n}\n\n", a.Field, a.Type)
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return fmt.Errorf("整形失敗: %w", err)
	}

	outPath := filepath.Join(pkgDir, "accessors_gen.go")
	if err := os.WriteFile(outPath, formatted, 0o644); err != nil {
		return fmt.Errorf("書き込み失敗: %w", err)
	}

	fmt.Printf("生成: %s（%d コンポーネント）\n", outPath, len(accessors))
	if len(skipped) > 0 {
		sort.Strings(skipped)
		fmt.Printf("型未検出でスキップ: %s\n", strings.Join(skipped, ", "))
	}
	return nil
}

// parseComponentFiles はcomponentsパッケージの.goファイル（テスト・生成物を除く）をパースする
func parseComponentFiles(pkgDir string) ([]*ast.File, error) {
	entries, err := os.ReadDir(pkgDir)
	if err != nil {
		return nil, fmt.Errorf("ディレクトリ読み込み失敗: %w", err)
	}
	fset := token.NewFileSet()
	var files []*ast.File
	for _, e := range entries {
		name := e.Name()
		if e.IsDir() || !strings.HasSuffix(name, ".go") {
			continue
		}
		if strings.HasSuffix(name, "_test.go") || name == "accessors_gen.go" {
			continue
		}
		f, err := parser.ParseFile(fset, filepath.Join(pkgDir, name), nil, 0)
		if err != nil {
			return nil, fmt.Errorf("パース失敗 %s: %w", name, err)
		}
		files = append(files, f)
	}
	return files, nil
}

// collectSliceComponentFields はComponents構造体の *ecs.SliceComponent フィールド名を集める
func collectSliceComponentFields(files []*ast.File) []string {
	var fields []string
	for _, file := range files {
		ast.Inspect(file, func(n ast.Node) bool {
			ts, ok := n.(*ast.TypeSpec)
			if !ok || ts.Name.Name != "Components" {
				return true
			}
			st, ok := ts.Type.(*ast.StructType)
			if !ok {
				return true
			}
			for _, f := range st.Fields.List {
				if !isSliceComponent(f.Type) || len(f.Names) == 0 {
					continue
				}
				fields = append(fields, f.Names[0].Name)
			}
			return false
		})
	}
	return fields
}

// isSliceComponent は型式が *ecs.SliceComponent かどうかを判定する
func isSliceComponent(expr ast.Expr) bool {
	star, ok := expr.(*ast.StarExpr)
	if !ok {
		return false
	}
	sel, ok := star.X.(*ast.SelectorExpr)
	if !ok {
		return false
	}
	pkgIdent, ok := sel.X.(*ast.Ident)
	if !ok {
		return false
	}
	return pkgIdent.Name == "ecs" && sel.Sel.Name == "SliceComponent"
}

// collectDeclaredTypes はパッケージ内で宣言された型名の集合を返す
func collectDeclaredTypes(files []*ast.File) map[string]bool {
	types := map[string]bool{}
	for _, file := range files {
		ast.Inspect(file, func(n ast.Node) bool {
			ts, ok := n.(*ast.TypeSpec)
			if !ok {
				return true
			}
			types[ts.Name.Name] = true
			return true
		})
	}
	return types
}
