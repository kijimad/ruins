// Package balance はゲームバランスのモンテカルロシミュレーションを提供する。
//
// # 目的
//
// パラメータ変更がゲーム体験に与える影響を、実際にプレイせずに定量的に評価する。
// 深度ごとのHP推移・突然死率・武器遷移を統計的に可視化し、
// 難易度曲線の妥当性を検証する。
//
// # アーキテクチャ
//
// ゲーム本体の戦闘ロジック（activity/attack.go）はECS依存のため、
// シミュレーション用にECS非依存の純粋関数として計算式を再実装している。
// 計算式の定数は本体側と同じ値をハードコードしており、乖離すると結果が不正確になる。
//
// # シミュレーションの階層
//
// 3段階の粒度でシミュレーションを提供する。
//
//   - 戦闘レベル: SimulateBattle は1対1の戦闘を模擬する。
//     プレイヤーと敵が交互に攻撃し、どちらかのHPが0以下になるまで繰り返す。
//     命中判定・ダメージ計算・クリティカルヒットを含む。
//
//   - フロアレベル: SimulateRun の各深度ループが1フロアに対応する。
//     フロアごとに 3+rand(3) 体の敵と連続戦闘し、
//     フロア終了後にアイテムテーブルから回復アイテムと武器を取得する。
//     より強い武器を拾った場合、以降の戦闘でその武器を使用する。
//
//   - ランレベル: RunSimulations はN回のランを実行し、RunStats に集約する。
//     各ランは独立したシード値を持ち、再現可能である。
//
// # 戦闘計算式
//
// 命中率:
//
//	hitRate = clamp(80 + (攻撃側器用 - 防御側敏捷) * 2 + 武器命中率 - 80, 5, 95)
//
// ダメージ:
//
//	damage = max(攻撃能力 + rand(1..6) + 武器ダメージ - 防御力, 1)
//	攻撃能力は近接武器なら筋力、遠距離武器なら感覚を参照する
//
// クリティカル:
//
//	命中ロールが5以下の場合、ダメージを1.5倍にする
//
// HP:
//
//	HP = 30 + 体力 * 8 + 筋力 + 感覚
//
// # フロアのアイテム取得
//
// 各フロア終了時に、対応するアイテムテーブルから 15+rand(9) 個のアイテムを抽選する。
// 回復アイテムはHPに加算し、武器アイテムはフロア内で最も強いものを選択する。
// 現在の武器よりダメージが高い場合のみ切り替える。
// この数値は mapplanner のアイテム配置ロジックと一致させている。
//
// # 統計指標
//
//   - 到達深度中央値: N回のランで到達した深度の中央値
//   - 突然死率: ある深度に到達したランのうち、その深度で死亡した割合。
//     敵の強さの急変を検出するのに有用である
//   - HP P5/P95: 残HPの下位5%・上位95%パーセンタイル。
//     最悪ケースと最良ケースの幅を示す
//   - 武器分布: 各深度で使用されている武器の割合。
//     装備成長の進行度を確認できる
//
// # 既知の制約
//
//   - スキル・属性相性・状態異常は未実装であり、実プレイとの乖離要因になる
//   - 敵の行動は最も重みの高い攻撃のみを使用する簡略化モデルである
//   - 防具の装備成長は未対応である
//   - 戦闘の行動順はプレイヤー先攻固定である
//
// # ファイル構成
//
//   - combat.go: 戦闘計算式とデータロード
//   - run.go: フロア・ランのシミュレーション実行
//   - stats.go: 統計集計とパーセンタイル計算
