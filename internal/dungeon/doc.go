//
// # 主な型
//
//   - Definition: ダンジョンの設定を保持する。名前、階層数、使用するマップ種類など
//   - PlannerWeight: マップ種類と出現重みのペア
//
// # 使い分け
//
// ダンジョン選択画面では registry から全ダンジョンを取得して表示する。
// ダンジョン開始時は selector でマップ種類を抽選する。
