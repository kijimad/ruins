// Package query はエンティティの状態読み取り・単純な状態変更・純粋計算を提供する。
//
// 他の world サブパッケージへの依存がない基盤レイヤーとして機能する。
// 依存方向: query（依存なし） ← lifecycle ← gameaction
//
// # 分類ルール
//
// このパッケージに配置する関数は以下の条件を全て満たす:
//   - エンティティの生成（AddEntities, NewEntity）や削除（DeleteEntity）を行わない
//   - エンティティの配置（Location系コンポーネントの排他切り替え）を行わない
//   - エンティティの座標やカメラ等の複数コンポーネントを連動して書き換えない
//   - 複数の lifecycle/gameaction 操作を組み合わせた複合ロジックではない
//
// # 具体例
//
// 配置する: コンポーネントの読み取り、単一コンポーネントの変更、純粋計算、価格計算
// 配置しない: プレイヤー移動（座標+カメラ連動 → lifecycle）、アイテム生成 → lifecycle、売買 → gameaction
package query
